async def generate_russian_task(topic: str, level: int) -> dict:
    """
    topic — тема из select (например 'орфография', 'грамматика', 'скорочтение', 'всё')
    level — уровень ребёнка (1-5)
    """
    cfg = LEVEL_CONFIG.get(level, LEVEL_CONFIG[1])

    # Если тема конкретная — фильтруем типы задач
    topic_filter = {
        "орфография":   ["zhi_shi", "soft_sign", "unstressed_vowel", "paired_consonant"],
        "слоги":        ["syllables"],
        "грамматика":   ["part_of_speech", "gender"],
        "скорочтение":  ["speed_read"],
    }
    allowed = topic_filter.get(topic, cfg["task_types"])
    # Пересечение с тем, что разрешено на текущем уровне
    available = [t for t in allowed if t in cfg["task_types"]]
    if not available:
        available = cfg["task_types"]

    task_type = random.choice(available)

    generators = {
        "zhi_shi":          make_zhi_shi,
        "syllables":        make_syllables,
        "soft_sign":        make_soft_sign,
        "unstressed_vowel": make_unstressed_vowel,
        "paired_consonant": make_paired_consonant,
        "part_of_speech":   make_part_of_speech,
        "gender":           make_gender,
        "speed_read":       lambda: make_speed_read(level),
    }

    return generators[task_type]()


