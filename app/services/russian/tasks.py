# ГЛАВНАЯ ФУНКЦИЯ
# ──────────────────────────────────────────

async def generate_russian_task(topic: str, level: int) -> dict:
    cfg = LEVEL_CONFIG.get(level, LEVEL_CONFIG[1])

    topic_filter = {
        "жи/ши":     ["zhi_shi"],
        "слоги":     ["syllables", "soft_sign"],
        "орфография":["unstressed_vowel", "paired_consonant"],
        "всё":       cfg["task_types"],
    }
    allowed   = topic_filter.get(topic, cfg["task_types"])
    available = [t for t in allowed if t in cfg["task_types"]]
    if not available:
        available = cfg["task_types"]

    task_type = random.choice(available)

    generators = {
        "zhi_shi":          make_zhi_shi,
        "syllables":        make_syllables,
        "soft_sign":        make_soft_sign,
        "unstressed_vowel": make_unstressed_vowel,
        "paired_consonant": make_paired_consonant,
    }
    return generators[task_type]()


