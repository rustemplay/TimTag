def make_zhi_shi() -> dict:
    tmpl, correct, options, hint = random.choice(ZHI_SHI_WORDS)
    random.shuffle(options)
    display = tmpl.replace("_", "__")
    return {
        "type": "zhi_shi",
        "question": f"Вставь нужную букву: «{display}»",
        "word_template": tmpl,
        "correct": correct,
        "options": options,
        "hint": hint,
        "ui": "buttons_text",   # кнопки с текстом (буква)
    }


def make_syllables() -> dict:
    word, count = random.choice(SYLLABLE_WORDS)
    # Варианты: правильный + соседние числа, не меньше 1
    variants = sorted({max(1, count - 1), count, count + 1})
    if len(variants) < 2:
        variants = [count, count + 1]
    return {
        "type": "syllables",
        "question": f"Сколько слогов в слове «{word}»?",
        "word": word,
        "correct": str(count),
        "options": [str(v) for v in variants],
        "hint": f"В слове «{word}» столько слогов, сколько гласных букв",
        "ui": "buttons_text",
    }


