async def explain_russian_mistake(task: dict, user_answer: str) -> str:
    question = task.get("question", "")
    correct  = task.get("correct", "")
    hint     = task.get("hint", "")

    prompt = (
        f"Ты добрый учитель русского языка для ребёнка 7-10 лет.\n"
        f"Задание: {question}\n"
        f"Ребёнок ответил: «{user_answer}». Правильный ответ: «{correct}».\n"
        f"Правило: {hint}\n"
        f"Объясни ошибку в 2 простых предложения. "
        f"Начни с 'Давай разберём 🤔'. Будь добрым."
    )
    try:
        return await chat(prompt, temperature=0.3)
    except Exception:
        return f"Давай разберём 🤔 {hint}. Попробуй ещё раз! 💪"