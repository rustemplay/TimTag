def make_zhi_shi() -> dict:
    tmpl, correct, options, hint = random.choice(ZHI_SHI_WORDS)
    opts = options[:]
    random.shuffle(opts)
    display = tmpl.replace("_", "…")
    return {
        "type":     "zhi_shi",
        "question": f"Вставь нужную букву: «{display}»",
        "correct":  correct,
        "options":  opts,
        "hint":     hint,
    }


def make_syllables() -> dict:
    word, count = random.choice(SYLLABLE_WORDS)
    variants = sorted({max(1, count - 1), count, count + 1})
    return {
        "type":     "syllables",
        "question": f"Сколько слогов в слове «{word}»?",
        "word":     word,
        "correct":  str(count),
        "options":  [str(v) for v in variants],
        "hint":     "Слогов столько, сколько гласных букв",
    }


def make_soft_sign() -> dict:
    display, correct, options, hint = random.choice(SOFT_SIGN_TASKS)
    opts = options[:]
    random.shuffle(opts)
    return {
        "type":     "soft_sign",
        "question": f"Как правильно написать: «{display}»?",
        "correct":  correct,
        "options":  opts,
        "hint":     hint,
    }


def make_unstressed_vowel() -> dict:
    tmpl, correct, options, check_word, hint = random.choice(UNSTRESSED_VOWEL_TASKS)
    opts = options[:]
